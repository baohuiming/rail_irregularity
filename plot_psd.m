[A,B,C,D,E,F,G] = deal(1.1029,-1.4709,0.5941,0.8480,3.8016,-0.2500,0.0112); % 左高低
fs=10000;        %采样频率
l=[1:1/fs:36,36.1:1/fs:100];    %波长范围，跳过敏感波长
% 经单位转化后的轨道谱公式
S=A.*(l.^2+B*l.^3+C*l.^4)./(l.^2.*(G*l.^4+F*l.^3+E*l.^2+D*l+1)); 
q=loglog(1./l,l.^2.*S);     %画波长与功率谱密度的双对数坐标图
xlabel('频率/m^{-1}')
ylabel('功率谱密度/[mm^2*m]')
grid on
